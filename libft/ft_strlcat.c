#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	sizef;

	i = 0;
	while (dst[i])
		i++;
	sizef = 0;
	while (src[sizef])
		sizef++;
	if (dstsize <= i)
		sizef += dstsize;
	else
		sizef += i;
	j = 0;
	while (src[j] && i + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (sizef);
}
